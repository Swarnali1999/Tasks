# Lists are containers to store a set of values of any data type.
# We create list using [].

name=['I',"Me",9, 5.4,False]

# printing using print() function
print(name)

# acccessing using index number
print(name[4])

# changing the value using index number
name[3]=45
print(name)

#list slicing
print(name[0:2])